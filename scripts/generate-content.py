def ts_string(s: str) -> str:
    """Безопасно экранирует строку для TypeScript."""
    return json.dumps(s, ensure_ascii=False)

def ts_dict(d: dict) -> str:
    """Сериализует произвольный словарь в TypeScript-объект."""
    parts = []
    for k, v in d.items():
        if isinstance(v, str):
            parts.append(f'{k}: {ts_string(v)}')
        elif isinstance(v, bool):
            parts.append(f'{k}: {"true" if v else "false"}')
        elif isinstance(v, int):
            parts.append(f'{k}: {v}')
        elif isinstance(v, list):
            if v and isinstance(v[0], dict):
                inner = ", ".join([ts_dict(item) for item in v])
                parts.append(f'{k}: [{inner}]')
            else:
                items = ", ".join([ts_string(item) for item in v])
                parts.append(f'{k}: [{items}]')
    return '{ ' + ", ".join(parts) + ' }'

def ts_block(block: dict, indent: str = "      ") -> str:
    return ts_dict(block)

